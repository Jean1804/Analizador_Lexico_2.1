    private void btn_analizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_analizarActionPerformed
        int fila = jTable1.getRowCount();
        for(int i =fila-1; i>=0; i--)
        {
            modelo.removeRow(i);
        }
        String [] info = new String[2];
        File archivo = new File("archivo.txt");
        PrintWriter escribir;
        try {
            escribir = new PrintWriter(archivo);
            escribir.print(txt_entrada.getText());
            escribir.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Analizador_Lexico.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            Reader lector = new BufferedReader(new FileReader("archivo.txt"));
            Lexer lexer = new Lexer(lector);
            String resultado = "";
            while (true) {
                Tokens tokens = lexer.yylex();
                if(tokens == null)
                {
                    //resultado += "FIN";
                    //txtresultado.setText(resultado);
                    return;
                }
                switch(tokens)
                {
                    case ERROR: 
                        resultado += "Simbolo no existe\n";
                        break;
                    case Identificador: case Numero: case Reservada:
                        info[0] = tokens.toString();
                        info[1] = lexer.lexeme;
                        modelo.addRow(info);
                        
                       break;
                    
                    default:
                        info[0] = "Token"; 
                        info[1] = tokens.toString();
                        modelo.addRow(info);
                      //  resultado += "Token: " + tokens + "\n";
                        break;
                     
                }
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Analizador_Lexico.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Analizador_Lexico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_analizarActionPerformed

